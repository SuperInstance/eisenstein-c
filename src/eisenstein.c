/* === Batch Operations (scalar fallback) === */

void e12_norm_batch(const E12* xs, int32_t* norms, int n) {
    for (int i = 0; i < n; i++) {
        norms[i] = e12_norm(xs[i]);
    }
}

void e12_check_batch_i8(const int8_t* vals, const Bound8* bounds,
                         int n, uint8_t* results) {
    for (int i = 0; i < n; i++) {
        results[i] = (vals[i] < bounds[i].lo || vals[i] > bounds[i].hi) ? 1 : 0;
    }
}
